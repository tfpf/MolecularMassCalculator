import java.awt.Font;

import javax.swing.JButton;

///////////////////////////////////////////////////////////////////////////////
// Class derived from `JButton'. The default font size of the text in a
// `JButton' is too large. `MolecularMassJButton' aims to fix this problem.
///////////////////////////////////////////////////////////////////////////////
public class MolecularMassJButton extends JButton
{
    ///////////////////////////////////////////////////////////////////////////
    // Constructor.
    ///////////////////////////////////////////////////////////////////////////
    MolecularMassJButton(String text)
    {
        super(text);
        this.setFont(new Font("DejaVu Sans", Font.PLAIN, 8));
    }

    ///////////////////////////////////////////////////////////////////////////
    // Constructor.
    ///////////////////////////////////////////////////////////////////////////
    MolecularMassJButton(String text, int size)
    {
        super(text);
        this.setFont(new Font("DejaVu Sans", Font.PLAIN, size));
    }
}

